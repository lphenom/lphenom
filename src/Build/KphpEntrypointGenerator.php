<?php

declare(strict_types=1);

namespace LPhenom\LPhenom\Build;

/**
 * Generates the KPHP entrypoint file (build/kphp-entrypoint.generated.php).
 *
 * Scans all project + vendor/lphenom files, filters by 'kphp' build target,
 * resolves dependencies, and writes require_once in correct order.
 *
 * The output file is a temporary build artifact (gitignored).
 *
 * KPHP-compatible: no Reflection, no dynamic class loading.
 */
final class KphpEntrypointGenerator
{
    /** @var BuildAnnotationScanner */
    private BuildAnnotationScanner $scanner;

    /** @var DependencyResolver */
    private DependencyResolver $resolver;

    /** @var string */
    private string $basePath;

    public function __construct(string $basePath, BuildAnnotationScanner $scanner, DependencyResolver $resolver)
    {
        $this->basePath = $basePath;
        $this->scanner  = $scanner;
        $this->resolver = $resolver;
    }

    /**
     * Create a generator with default configuration.
     */
    public static function createDefault(string $basePath): self
    {
        return new self(
            $basePath,
            BuildAnnotationScanner::createDefault($basePath),
            DependencyResolver::createFromComposer($basePath)
        );
    }

    /**
     * Generate the KPHP entrypoint and write to file.
     *
     * @param string $outputPath Absolute path for the generated file
     * @return int Number of files included
     */
    public function generate(string $outputPath): int
    {
        $files  = $this->scanner->scanForTarget('kphp');
        $sorted = $this->resolver->topologicalSort($files);

        $content = $this->renderEntrypoint($sorted);

        $dir = dirname($outputPath);
        if (!is_dir($dir)) {
            mkdir($dir, 0755, true);
        }

        file_put_contents($outputPath, $content);

        return count($sorted);
    }

    /**
     * Get the list of files that would be included for kphp target (sorted).
     *
     * @return string[]
     */
    public function getKphpFiles(): array
    {
        $files = $this->scanner->scanForTarget('kphp');
        return $this->resolver->topologicalSort($files);
    }

    /**
     * Render the entrypoint PHP file content.
     *
     * @param string[] $sortedFiles Absolute paths in dependency order
     */
    private function renderEntrypoint(array $sortedFiles): string
    {
        $lines   = [];
        $lines[] = '<?php';
        $lines[] = '';
        $lines[] = '/**';
        $lines[] = ' * KPHP entrypoint for lphenom/lphenom.';
        $lines[] = ' *';
        $lines[] = ' * AUTO-GENERATED by BuildKphpCommand — do not edit manually.';
        $lines[] = ' * Generated: ' . date('Y-m-d H:i:s');
        $lines[] = ' *';
        $lines[] = ' * All files must be required explicitly in dependency order.';
        $lines[] = ' * KPHP does not support Composer PSR-4 autoloading.';
        $lines[] = ' */';
        $lines[] = '';
        $lines[] = 'declare(strict_types=1);';
        $lines[] = '';

        // Group files by package for readability
        $groups = $this->groupByPackage($sortedFiles);

        foreach ($groups as $package => $files) {
            $lines[] = '// === ' . $package . ' ===';
            foreach ($files as $file) {
                $relative = $this->makeRelativePath($file);
                $lines[] = "require_once __DIR__ . '/" . $relative . "';";
            }
            $lines[] = '';
        }

        // Smoke test
        $lines[] = '// === Smoke test ===';
        $lines[] = "\$config = new \\LPhenom\\Core\\Config\\Config([";
        $lines[] = "    'app' => ['name' => 'lphenom']";
        $lines[] = ']);';
        $lines[] = '';
        $lines[] = "\$container = new \\LPhenom\\Core\\Container\\Container();";
        $lines[] = "\$app = new \\LPhenom\\LPhenom\\Application(\$container, \$config, '/tmp');";
        $lines[] = '';
        $lines[] = "echo 'LPhenom kernel KPHP smoke test: OK' . PHP_EOL;";
        $lines[] = '';

        return implode("\n", $lines);
    }

    /**
     * Group files by their package name for organized output.
     *
     * @param string[] $files
     * @return array<string, string[]>
     */
    private function groupByPackage(array $files): array
    {
        /** @var array<string, string[]> $groups */
        $groups = [];

        foreach ($files as $file) {
            $package = $this->detectPackage($file);
            if (!isset($groups[$package])) {
                $groups[$package] = [];
            }
            $groups[$package][] = $file;
        }

        return $groups;
    }

    /**
     * Detect which package a file belongs to.
     */
    private function detectPackage(string $file): string
    {
        // Check if it's in vendor/lphenom/PACKAGE/src/...
        $vendorPrefix = $this->basePath . '/vendor/lphenom/';
        if (strpos($file, $vendorPrefix) === 0) {
            $rest = substr($file, strlen($vendorPrefix));
            $slashPos = strpos($rest, '/');
            if ($slashPos !== false) {
                return 'lphenom/' . substr($rest, 0, $slashPos);
            }
        }

        // Check if it's in src/ (the kernel itself)
        $srcPrefix = $this->basePath . '/src/';
        if (strpos($file, $srcPrefix) === 0) {
            return 'lphenom/lphenom (kernel)';
        }

        return 'unknown';
    }

    /**
     * Make a path relative to the build/ directory (where entrypoint lives).
     */
    private function makeRelativePath(string $file): string
    {
        // The entrypoint is in build/, so paths are relative from build/
        // __DIR__ will be build/, so we need ../
        $basePath = $this->basePath;

        if (strpos($file, $basePath) === 0) {
            $relative = substr($file, strlen($basePath) + 1); // +1 for /
            return '../' . $relative;
        }

        return $file;
    }
}

